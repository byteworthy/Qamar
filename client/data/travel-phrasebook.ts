/** Get all phrases for a given category */
export function getPhrasesByCategory(category: TravelCategory): TravelPhrase[] {
  return TRAVEL_PHRASES.filter((p) => p.category === category);
}

/** Search phrases by query in any language */
export function searchPhrases(
  query: string,
  sourceLang: "en" | "es",
): TravelPhrase[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  return TRAVEL_PHRASES.filter((p) => {
    const sourceText = sourceLang === "en" ? p.en : p.es;
    return (
      sourceText.toLowerCase().includes(q) ||
      p.ar.includes(q) ||
      p.transliteration.toLowerCase().includes(q)
    );
  });
}

